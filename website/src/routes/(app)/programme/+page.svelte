        <h2 class="text-2xl font-bold mb-4">2.0 Qui sommes-nous ?</h2>

        <!-- Theme -->
        <h3 id="theme" class="text-xl font-bold mb-2">2.1 Thème</h3>
        <p class="leading-7">
            Nous avons décidé de conserver le nom Kraken car nous partageons les
            mêmes valeurs que les précédents Kraken, qui seront présents pour
            nous soutenir. Nous souhaitons apporter de la joie et de la bonne
            humeur à tous les étudiants. Notre nom s’inspire d’une créature
            mythologique ayant pour forme une pieuvre géante. Notre thème, les
            pirates, a pour objectif de donner une direction commune à
            l’ensemble de nos équipes. De cette manière, les équipes de
            communication, de décoration et d’organisation d’activités feront
            presque systématiquement allusion aux pirates.<br />Mais pourquoi
            les pirates ? En réalité, les pirates avaient toutes les qualités
            dont un BDE peut rêver ou presque. En effet, les pirates savent agir
            aux moments les plus opportuns. Un groupe soudé capable de réagir
            vite aux imprévus sans jamais se livrer à leurs adversaires. Ils
            parcourent les flots afin de récupérer toujours plus de trésors et
            surtout de dénicher LA perle rare.<br />Par-dessus tout, les pirates
            sont connus pour leur bonne humeur et leur envie constante de jouer
            de la musique, de danser et de chanter. Ainsi, les Kraken ont bien
            pour intention de s’imprégner de cette culture afin que nous aussi,
            nous puissions nous emparer du plus grand des trésors : le cœur de
            nos étudiants.
        </p>

