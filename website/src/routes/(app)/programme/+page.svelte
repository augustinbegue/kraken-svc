    <section id="introduction" class="mb-8">
        <h2 class="text-2xl font-bold mb-4">1.0 Introduction</h2>
        <p class="leading-7">
            Le Bureau Des Élèves occupe un poste de premier ordre au sein de la
            vie associative d’une école. En effet, il est présent pour permettre
            aux étudiants de s’évader un court moment et de se créer des
            souvenirs qui resteront à jamais gravés dans leur mémoire. Kraken a
            pour vocation d’animer, de rassembler et de se positionner comme un
            acteur principal au sein de l’EPITA. Dans cette optique, nous vous
            soumettons notre candidature aux élections du BDE 2024. Nous sommes
            préparés, expérimentés, déterminés, et avant tout : nous formons un
            groupe solide où la cohésion est le maître mot. Notre vision du BDE
            est celle d’une équipe soudée, investie, organisée et prête à
            représenter les valeurs de l’EPITA, nos valeurs. De ce fait, toutes
            les promotions de 2024 à 2028 occupent une place à forte
            responsabilité. Notre BDE comporte principalement des personnes
            expérimentées ayant pour la quasi-totalité déjà eu l’occasion de
            s’investir dans l’événementiel que ce soit dans le monde associatif
            comme dans le monde professionnel. Cette expérience nous permet
            notamment d’avoir une compréhension de l’ensemble des enjeux
            tournant autour de la campagne et d’un mandat en tant que BDE de
            l’EPITA.
        </p>
    </section>

