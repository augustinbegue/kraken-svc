        <!-- Week Details -->
        <h3 id="week-details" class="text-xl font-bold mb-2">
            3.1 Déroulement de la semaine
        </h3>

        <p class="leading-7">
            La semaine d'élection va se dérouler du jeudi 14 décembre au
            mercredi 20 décembre. Notre campagne sera portée sur l’inclusion et
            l’envie d’offrir aux élèves. Pour cela, tout consommable que nous
            allons acheter sera vendu au prix le plus bas possible.
            <br />
            Lors de la campagne, nous mettrons en place un service téléphonique pour
            rendre service à nos étudiants. Par exemple, nous effectuerons tout au
            long de la semaine des livraisons de kebab aux étudiants nous ayant appelés
            et envoyé leur commande.
            <br />
            En ce qui concerne le distanciel, un live sera organisé chaque jour :
            une FAQ, une soirée jeu télévisé, et une soirée tournoi jeu vidéo. Il
            y aura de plus tout au long de la semaine un serveur Minecraft qui sera
            ouvert sur lesquels des défis seront lancés. Il y aura de plus du samedi
            au dimanche un live 24h caritatif pendant lequel tous les dons seront
            reversés à une association.
        </p>
    </section>

