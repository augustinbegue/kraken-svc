        <h2 class="text-2xl font-bold mb-4">
            4.0 Déroulement du reste de l'année
        </h2>

        <!-- WEI -->
        <section id="wei" class="mb-4">
            <h3 class="text-xl font-bold mb-2">4.1 Le WEI</h3>
            Le WEI est un élément incontournable de l'ING1. Cet événement démarre
            l'année, reliant les campus de Paris, Lyon, Rennes Strasbourg et Toulouse,
            il est l'événement idéal permettant à tous les étudiants d'apprendre
            à se connaître tout en créant une cohésion au sein de la promotion. Nous
            sommes déjà en contact avec différents prestataires afin de comparer
            de manière objective leurs différentes propositions dans l'objectif d'assurer
            un WEI de qualité exceptionnelle.
            <br />
            Grâce à l'expérience que nous avons pu acquérir par les différentes associations
            et le staff que nous avons effectué lors des précédents WEI, nous avons
            les points clés en main pour produire un WEI de qualité.
            <br />
            Ce que nous aimerions accomplir pour le WEI :<br />
            Un week-end de 3 jours (en bord de mer), si possible sur une île (Oléron,
            Ré, …) sur la côte Atlantique dans un lieu réservé pour l'occasion. Le
            lieu sera au maximum à six heures de route du campus de Paris. Les activités
            seront principalement aquatiques, de la voile, du kayak, du surf. Nos
            soirées seront explosives et se mêleront au décor du lieu.
        </section>

        <!-- WEIP -->
        <section id="weip" class="mb-4">
            <h3 class="text-xl font-bold mb-2">4.2 Le WEIP</h3>
            Dans le cadre du weekend d’intégration des prépas, nous avons convenu
            de répartir les étudiants en équipes par classe qui évolueront ensemble.
            Cela permettra d’encadrer efficacement l’intégration de ces derniers,
            et ce en toute sécurité.
        </section>

        <!-- Parties -->
        <section id="parties" class="mb-4">
            <h3 class="text-xl font-bold mb-2">4.3 Les soirées</h3>
            Lors de notre mandat nous avons prévu d’organiser 7 soirées à l’Under
            afin de se constituer un fond de roulement solide afin de financer une
            partie du gala que nous souhaiterions faire dans une salle de soirée
            parisienne. De plus ces soirées aideront à financer une partie du WEI
            afin de baisser les coûts pour l’école et surtout les étudiants. L’objectif
            étant de produire un bénéfice moyen de 2500 euros par soirée.
        </section>

        <!-- Afterworks -->
        <section id="afterworks" class="mb-4">
            <h3 class="text-xl font-bold mb-2">4.4 Les afterworks</h3>
            Lors de notre mandat nous souhaitons aussi organiser des afterwork. En
            effet, la vie étudiante ne se résume pas qu’aux soirées, nous avons aussi
            besoin d’événements pour que les étudiants se rencontrent, rencontrent
            leurs associations, ainsi que leur BDE. Ces moments seront pour nous
            des moments de partage et d’échange respectueux avec nos étudiants afin
            de mieux les connaître et avoir leurs retours sur les activités que nous
            leurs organisons.
        </section>

        <!-- Promotion Pulls -->
        <section id="promotion-pulls" class="mb-4">
            <h3 class="text-xl font-bold mb-2">4.5 Pulls de promotion</h3>
            Nous souhaitons cette année remettre en place les pulls de promotion
            qui pour nous sont partie intégrante de l’appartenance à l’école. Cela
            permettra aux différentes promotions de se reconnaître entre elles et
            par conséquent resserrer les liens entre ces dernières.
        </section>

        <!-- World Week -->
        <section id="world-week" class="mb-4">
            <h3 class="text-xl font-bold mb-2">4.6 La semaine du monde</h3>
            Lors de cette semaine, l’international sera à l’honneur. Chaque jour,
            un nouveau continent sera représenté et des événements tournant autour
            de ces derniers seront organisés afin de faire découvrir toutes les cultures
            à nos étudiants. Nous pourrons inviter et inclure tous nos étudiants
            internationaux si souvent oubliés.
        </section>

        <!-- Sponsorship -->
        <section id="sponsorship" class="mb-4">
            <h3 class="text-xl font-bold mb-2">4.7 Le parrainage</h3>
            Lors de notre mandat nous souhaitons mettre en place le parrainage entre
            étudiants de différentes promotions afin que les nouveaux étudiants puissent
            avoir des référents et puissent poser leurs questions sur tout ce qui
            concerne EPITA, son environnement, et l’avenir qu’ils y envisagent.
        </section>

        <!-- Christmas Gala -->
        <section id="christmas-gala" class="mb-4">
            <h3 class="text-xl font-bold mb-2">4.8 Gala de Noël</h3>
            Si nous sommes élus cette année, nous souhaiterions mettre en place un
            Gala de Noël qui pour nous est un événement incontournable qui devrait
            faire son retour à l’EPITA. En effet, il s’agit d’un événement totalement
            différent de tous ceux que nous comptons organiser. C’est pour nous un
            nouveau défi à relever. Nous aimerions le faire dans une salle hors de
            l’EPITA. Pour cela, nous avons déjà des partenaires qui peuvent nous
            louer des salles à moindre coût et qui sont prêts à nous sponsoriser
            sur ce gala.
        </section>

        <!-- Apprentices -->
        <section id="apprentices" class="mb-4">
            <h3 class="text-xl font-bold mb-2">4.9 Les apprentis</h3>
            Les alternants sont un grand groupe ayant une place importante dans l’EPITA.
            Ils ont trop longtemps été laissés de côté. Nous souhaitons cette année
            essayer de les inclure au maximum à l’aide de l’association BÉABA dont
            nous sommes très proches.
        </section>

        <!-- VSS Fight -->
        <section id="vss-fight" class="mb-4">
            <h3 class="text-xl font-bold mb-2">4.10 La lutte contre les VSS</h3>
            La lutte contre les violences sexistes et sexuelles est une cause qui
            nous tient à cœur et un combat que nous mènerons tout au long de notre
            mandat. Aucun écart de tout type qu’il soit ne sera toléré et des sanctions
            fermes seront appliquées à tout contrevenant
        </section>

        <!-- Commitments -->
        <section id="commitments" class="mb-4">
            <h3 class="text-xl font-bold mb-2">4.11 Nos engagements</h3>
            La famille Kraken s’engage à ne jamais cesser ses activités régulières
            pendant son mandat en conservant la cohésion de notre groupe et par conséquent
            l’investissement de celui-ci. Nous entretiendrons l’engagement de chacun
            des membres de cette famille en leur offrant des responsabilités importantes
            et en les réunissant au moins une fois tous les 2 mois pour un team building.<br
            />

            La famille Kraken s’engage à mettre en place tout ce qui est en son
            pouvoir afin d’assurer que l’égalité entre les hommes et les femmes
            soit respectée lors de nos événements. De même, les Kraken
            s’engagent à ce que l’inclusivité sous sa tutelle soit assurée ; les
            différences de chacun sont la force de tous, nous veillons à être un
            exemple sur ce point. Un de nos points d’attention pour ce mandat
            est aussi la sensibilisation aux VSS lors de nos différents
            évènements.<br />

            La famille Kraken s’engage à proposer des soirées / afterwork
            innovants ayant des activités et des menus personnalisés à notre
            image, en collaboration éventuelle avec nos voisins : Sup’Biotech et
            EPITECH. Nos événements seront un moment de partage avec nos
            étudiants et nous les utiliserons afin de mettre en lumière des
            causes qui nous sont chères. Les entrées à nos soirées resteront
            toujours abordables pour nos étudiants (hors cas exceptionnels tel
            que le gala). De même pour les consommations que nous proposerons en
            soirée. Lors de ces événements, toute association souhaitant y
            participer sera la bienvenue.<br />

            Un de nos points d’attention pour ce mandat est aussi la
            sensibilisation aux VSS lors de nos différents évènements.
            <br />
            L’engouement de nos membres est exceptionnel. Comme les pirates, nous
            rêvons d’un trésor grandiose et nous sommes prêts à parcourir les océans
            à la recherche de celui-ci. Kraken fera de l’année 2024 une année dont
            chaque étudiant se souviendra puisque nous propagerons au sein des campus
            la bonne humeur qui exalte de notre liste.
            <br />
            Pour finir et afin que tout ce qui a précédemment été énoncé soit respecté,
            un règlement interne à l’association sera rédigé et, après lecture et
            validation de ce règlement par le responsable de la vie associative,
            signé par l’ensemble des membres, responsables, bureau et nouvelles recrues.
        </section>
